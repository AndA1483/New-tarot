        return "พลังและความแข็งแกร่งของร่างกาย สามารถควบคุมและปรับปรุงสุขภาพได้ด้วยตัวเอง";
      if (cardName.includes("จักรพรรดินี"))
        return "สุขภาพแข็งแกร่งและเต็มไปด้วยพลัง โดยเฉพาะสุขภาพของผู้หญิงและเรื่องการเจริญพันธุ์";
      if (cardName.includes("คู่รัก"))
        return "ความสมดุลของร่างกายและจิตใจ การดูแลสุขภาพร่วมกับคนรัก";
