// ฟังก์ชันสำหรับดึงความหมายตามไพ่และตำแหน่ง
export const getPositionMeaning = (cardId, positionIndex) => {
  const meanings = positionMeaningsData[cardId];
  if (!meanings || !meanings[positionIndex]) {
    return "ไม่พบความหมายสำหรับไพ่ใบนี้";
  }
  return meanings[positionIndex];
};

